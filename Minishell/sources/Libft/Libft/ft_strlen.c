#include "original_libft.h"

/*
PARAMETERS
str:	The string whose length is to be calculated.

DESCRIPTION
The strlen() function calculates the length of the string pointed to by str,
excluding the terminating null byte ('\0').

RETURN VALUE
The strlen() function returns the number of bytes in the string pointed to
by str.

EXPLANATION
Strings end with the character '\0', so the while loop increments length until
the character '\0' is found.
*/
size_t	ft_strlen(const char *str)
{
	size_t	length;

	length = 0;
	while (str[length] != '\0')
		length++;
	return (length);
}
