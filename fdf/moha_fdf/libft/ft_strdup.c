#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*s2;

	if (!s)
		return (NULL);
	i = 0;
	s2 = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (s2 == NULL)
		return (NULL);
	while (s[i])
	{
		s2[i] = s[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
